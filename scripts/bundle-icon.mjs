#!/usr/bin/env node
// Reads icon.png (512×512) and icon-48.png (48×48) from the project root and
// writes src/icon-bundled.ts with two base64 data URI constants.
// Idempotent — safe to run anywhere.
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const iconPath = join(__dirname, "..", "icon.png");
const icon48Path = join(__dirname, "..", "icon-48.png");
const srcDir = join(__dirname, "..", "src");
const outFile = join(srcDir, "icon-bundled.ts");

if (!existsSync(iconPath)) {
  console.error(`bundle-icon: icon.png not found at ${iconPath}`);
  process.exit(1);
}
if (!existsSync(icon48Path)) {
  console.error(`bundle-icon: icon-48.png not found at ${icon48Path}`);
  process.exit(1);
}

const data = readFileSync(iconPath);
const data48 = readFileSync(icon48Path);
const dataUri = `data:image/png;base64,${data.toString("base64")}`;
const dataUri48 = `data:image/png;base64,${data48.toString("base64")}`;

const content = [
  "// AUTO-GENERATED by scripts/bundle-icon.mjs — do not edit by hand.",
  "// Edit icon.png / icon-48.png and re-run `npm run build` (or any npm script — the pre-hook regenerates).",
  "",
  `export const ICON_DATA_URI: string = ${JSON.stringify(dataUri)};`,
  `export const ICON_DATA_URI_48: string = ${JSON.stringify(dataUri48)};`,
  "",
].join("\n");

writeFileSync(outFile, content, "utf8");
console.error(
  `bundle-icon: wrote ${data.length}-byte PNG (512×512) and ${data48.length}-byte PNG (48×48) as data URIs to ${outFile}`,
);
