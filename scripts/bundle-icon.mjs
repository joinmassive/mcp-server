#!/usr/bin/env node
// Reads icon.png from the project root and writes src/icon-bundled.ts with a
// base64 data URI constant. Idempotent — safe to run anywhere.
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const iconPath = join(__dirname, "..", "icon.png");
const srcDir = join(__dirname, "..", "src");
const outFile = join(srcDir, "icon-bundled.ts");

if (!existsSync(iconPath)) {
  console.error(`bundle-icon: icon.png not found at ${iconPath}`);
  process.exit(1);
}

const data = readFileSync(iconPath);
const dataUri = `data:image/png;base64,${data.toString("base64")}`;

const content = [
  "// AUTO-GENERATED by scripts/bundle-icon.mjs — do not edit by hand.",
  "// Edit icon.png and re-run `npm run build` (or any npm script — the pre-hook regenerates).",
  "",
  `export const ICON_DATA_URI: string = ${JSON.stringify(dataUri)};`,
  "",
].join("\n");

writeFileSync(outFile, content, "utf8");
console.error(`bundle-icon: wrote ${data.length}-byte PNG as data URI to ${outFile}`);
