#!/usr/bin/env node
// Reads src/resources/*.md and writes src/resources/bundled.ts with one
// exported string constant per file. Idempotent — safe to run anywhere.
import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join, basename, extname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcDir = join(__dirname, "..", "src", "resources");
const outFile = join(srcDir, "bundled.ts");

if (!existsSync(srcDir)) mkdirSync(srcDir, { recursive: true });

const files = readdirSync(srcDir)
  .filter((f) => extname(f) === ".md")
  .sort();

const lines = [
  "// AUTO-GENERATED by scripts/bundle-resources.mjs — do not edit by hand.",
  "// Edit the matching src/resources/*.md file and re-run `npm run build` (or any npm script — the pre-hook regenerates).",
  "",
];

for (const file of files) {
  const name = basename(file, ".md").toUpperCase().replace(/-/g, "_") + "_MD";
  const content = readFileSync(join(srcDir, file), "utf8");
  // Use JSON.stringify to safely embed any UTF-8 content as a TS string literal.
  lines.push(`export const ${name}: string = ${JSON.stringify(content)};`);
}

lines.push("");
writeFileSync(outFile, lines.join("\n"), "utf8");
console.error(`bundle-resources: wrote ${files.length} markdown file(s) to ${outFile}`);
